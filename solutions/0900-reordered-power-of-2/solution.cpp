class Solution {
public:
    bool reorderedPowerOf2(int n) {
        static const vector<unordered_map<int,int>> power_maps = {
            {{1,1}},                                   // 1
            {{2,1}},                                   // 2
            {{4,1}},                                   // 4
            {{8,1}},                                   // 8
            {{1,1},{6,1}},                             // 16
            {{3,1},{2,1}},                             // 32
            {{6,1},{4,1}},                             // 64
            {{1,1},{2,1},{8,1}},                       // 128
            {{2,1},{5,1},{6,1}},                       // 256
            {{5,1},{1,1},{2,1}},                       // 512
            {{1,1},{0,1},{2,1},{4,1}},                 // 1024
            {{2,1},{0,1},{4,1},{8,1}},                 // 2048
            {{4,1},{0,1},{9,1},{6,1}},                 // 4096
            {{8,1},{1,1},{9,1},{2,1}},                 // 8192
            {{1,1},{6,1},{3,1},{8,1},{4,1}},           // 16384
            {{3,1},{2,1},{7,1},{6,1},{8,1}},           // 32768
            {{6,2},{5,2},{3,1}},                       // 65536
            {{1,2},{3,1},{0,1},{7,1},{2,1}},           // 131072
            {{2,2},{6,1},{1,1},{4,2}},                 // 262144
            {{5,1},{2,2},{4,1},{8,2}},                 // 524288
            {{1,1},{0,1},{4,1},{8,1},{5,1},{7,1},{6,1}}, // 1048576
            {{2,2},{0,1},{9,1},{7,1},{1,1},{5,1}},     // 2097152
            {{4,3},{1,1},{9,1},{3,1},{0,1}},           // 4194304
            {{8,4},{3,1},{6,1},{0,1}},                 // 8388608
            {{1,2},{6,2},{7,3},{2,1}},                 // 16777216
            {{3,3},{5,2},{4,2},{2,1}},                 // 33554432
            {{6,2},{7,1},{1,1},{0,1},{8,2},{4,1}},     // 67108864
            {{1,2},{3,1},{4,1},{2,2},{7,2},{8,1}},     // 134217728
            {{2,1},{6,2},{8,1},{4,2},{3,1},{5,2}},     // 268435456
            {{5,1},{3,1},{6,1},{8,1},{7,1},{0,1},{9,1},{1,1},{2,1}}, // 536870912
            {{1,2},{0,1},{7,2},{3,1},{4,2},{8,1},{2,1}}  // 1073741824
        };

        unordered_map<int,int> targetmap;
        for (int k = n; k > 0; k /= 10) {
            targetmap[k % 10]++;
        }

        for (const auto& pm : power_maps) {
            if (pm == targetmap) return true;
        }
        return false;
    }
};

